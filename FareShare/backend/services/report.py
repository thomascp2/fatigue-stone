"""
Dispute Report generator.

Takes extraction + comp analysis data and produces:
1. A Markdown dispute report (human-readable summary)
2. A PDF version of that report (via ReportLab)

The report is the deliverable for Phase 1 — it summarizes the finding,
lists the comps, and lays out the legal grounds for appeal.
"""

